function horas_Minutos(horas) {
  return horas * 60;
}

function minutos_Segundos(minutos) {
  return minutos * 60;
}

function horas_Segundos(horas) {
  return horas * 3600;
}

console.log(horas_Minutos(2)+ " minutos");
console.log(minutos_Segundos(5)+ " segundos");
console.log(horas_Segundos(1) + " segundos");